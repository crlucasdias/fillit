#include "libft.h"

char	*ft_strcpy(char *dest, char *from)
{
	int i;

	i = 0;
	while (from[i] != '\0')
	{
		dest[i] = from[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
